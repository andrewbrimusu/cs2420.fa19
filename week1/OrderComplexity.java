public class OrderComplexity {
    public static void main(String args[]) {
        int n = 1000;
        for(int i=0; i<n; i++) {
            for(int j=0; j<n; j++) {
                System.out.println("hello");
            }
        }
        for(int i=0; i<n; i++) {
            System.out.println("hello");
        }
    }
}